package com.nanolaba.nrg.widgets;

import com.nanolaba.logging.LOG;
import com.nanolaba.nrg.core.Generator;
import org.apache.commons.lang3.StringUtils;
import org.junit.jupiter.api.Test;

import java.io.File;
import java.io.IOException;
import java.nio.charset.StandardCharsets;

import static org.junit.jupiter.api.Assertions.*;

class ImportWidgetTest {

    @Test
    public void testImportAndTOC() throws IOException {
        File sourceFile = new File(getClass().getClassLoader().getResource("ImportWidgetTest/testTOC/test.src.md").getFile());
        Generator generator = new Generator(sourceFile, StandardCharsets.UTF_8);

        String bodyEn = generator.getResult("en").getContent().toString();
        LOG.info(bodyEn);
        assertTrue(bodyEn.contains("## HEADER1"));
        assertTrue(bodyEn.contains("## IGNORED_HEADER1"));
        assertTrue(bodyEn.contains("## HEADER2"));
        assertTrue(bodyEn.contains("## IGNORED_HEADER2"));
        assertTrue(bodyEn.contains("## HEADER3"));
        assertTrue(bodyEn.contains("## HEADER4"));
        assertTrue(bodyEn.contains(". [HEADER1](#header1)"));
        assertTrue(bodyEn.contains(". [HEADER2](#header2)"));
        assertTrue(bodyEn.contains(". [HEADER3](#header3)"));
        assertTrue(bodyEn.contains(". [HEADER4](#header4)"));

        assertFalse(bodyEn.contains(". [IGNORED_HEADER1](#ignored"));
        assertFalse(bodyEn.contains(". [IGNORED_HEADER2](#ignored"));
    }

    @Test
    public void testIgnoreMarkersInsideImportedFile() throws IOException {
        File sourceFile = new File(getClass().getClassLoader().getResource("ImportWidgetTest/testIgnoreInsideImport/test.src.md").getFile());
        Generator generator = new Generator(sourceFile, StandardCharsets.UTF_8);

        String body = generator.getResult("en").getContent().toString();
        LOG.info(body);
        assertTrue(body.contains("imported-before"));
        assertTrue(body.contains("imported-after"));
        assertFalse(body.contains("imported-inline-note"));
        assertFalse(body.contains("author note line 1"));
        assertFalse(body.contains("author note line 2"));
        assertFalse(body.contains("nrg.ignore"));
    }

    @Test
    public void testImportForDifferentLanguages() throws IOException {
        File sourceFile = new File(getClass().getClassLoader().getResource("ImportWidgetTest/testImportForDifferentLanguages/test.src.md").getFile());
        Generator generator = new Generator(sourceFile, StandardCharsets.UTF_8);

        String bodyEn = generator.getResult("en").getContent().toString();
        LOG.info(bodyEn);
        assertTrue(bodyEn.contains("Test"));

        String bodyRu = generator.getResult("ru").getContent().toString();
        LOG.info(bodyRu);
        assertTrue(bodyRu.contains("Тест"));
    }

    @Test
    public void testImportWidget() throws IOException {
        File sourceFile = new File(getClass().getClassLoader().getResource("ImportWidgetTest/README.src.md").getFile());
        Generator generator = new Generator(sourceFile, StandardCharsets.UTF_8);

        String bodyEn = generator.getResult("en").getContent().toString();
        LOG.info(bodyEn);

        assertEquals(1, StringUtils.countMatches(bodyEn, "<!-- This file was automatically generated by Nanolaba Readme Generator"));

        assertTrue(bodyEn.contains("<!--testComment1-->"));
        assertTrue(bodyEn.contains("<!--@testEscapedParam1=1-->"));

        // test.txt
        assertTrue(bodyEn.contains("Hello world!"));

        // folder/test.txt
        assertTrue(bodyEn.contains("Text from file in a folder."));

        // folder/testWin1251.txt
        assertTrue(bodyEn.contains("Текст в кодировке win1251"));

        // folder/test.src.md
        assertFalse(bodyEn.contains("<!--@testParameter=foo-->"));
        assertFalse(bodyEn.contains("${testParameter}"));
        assertTrue(bodyEn.contains("foobar"));
        assertFalse(bodyEn.contains("test1: ${globalParameter}"));
        assertTrue(bodyEn.contains("test1: This parameter is visible in all imported files"));
        assertTrue(bodyEn.contains("<!--testComment2-->"));
        assertTrue(bodyEn.contains("<!--@testEscapedParam2=1-->"));

        // folder/test-no-generation.src.md
        assertFalse(bodyEn.contains("<!--@unprocessedParameter=bar-->"));
        assertTrue(bodyEn.contains("foo${unprocessedParameter}"));
        assertTrue(bodyEn.contains("<!--testComment3-->"));
    }

    private String render(String fixturePath) throws IOException {
        File sourceFile = new File(getClass().getClassLoader().getResource(fixturePath).getFile());
        Generator generator = new Generator(sourceFile, StandardCharsets.UTF_8);
        return generator.getResult("en").getContent().toString();
    }

    @Test
    public void wrapTrueWithRegionWrapsInJavaFence() throws IOException {
        String body = render("ImportWidgetTest/extension/README-wrap-true-region.src.md");
        assertTrue(body.contains("```java"), body);
        assertTrue(body.contains("public void greet() {"), body);
        assertTrue(body.contains("System.out.println(\"hello\");"), body);
        // Markers stripped
        assertFalse(body.contains("nrg:begin:greet"));
        assertFalse(body.contains("nrg:end:greet"));
        // Closing fence
        String sep = System.lineSeparator();
        assertTrue(body.contains("```" + sep) || body.endsWith("```"), body);
    }

    @Test
    public void wrapTrueWithLinesWrapsInJavaFence() throws IOException {
        String body = render("ImportWidgetTest/extension/README-wrap-true-lines.src.md");
        assertTrue(body.contains("```java"), body);
        assertTrue(body.contains("public class Sample {"), body);
    }

    @Test
    public void wrapTrueWithLangWrapsFullFile() throws IOException {
        String body = render("ImportWidgetTest/extension/README-wrap-true-lang.src.md");
        assertTrue(body.contains("```text"), body);
        assertTrue(body.contains("plain text line 1"), body);
        assertTrue(body.contains("plain text line 3"), body);
    }

    @Test
    public void wrapDefaultsToFalse() throws IOException {
        // No wrap attribute — defaults to false; region content is emitted without a fence.
        String body = render("ImportWidgetTest/extension/README-wrap-default-region.src.md");
        assertFalse(body.contains("```java"), body);
        assertTrue(body.contains("public void greet() {"), body);
    }

    @Test
    public void wrapFalseDisablesFenceEvenWithRegion() throws IOException {
        String body = render("ImportWidgetTest/extension/README-wrap-false-region.src.md");
        assertFalse(body.contains("```java"), body);
        assertTrue(body.contains("public void greet() {"), body);
    }

    @Test
    public void wrapTrueWithoutSelectionWrapsFullFile() throws IOException {
        String body = render("ImportWidgetTest/extension/README-wrap-true-fullfile.src.md");
        assertTrue(body.contains("```java"), body);
        assertTrue(body.contains("package example;"), body);
    }

    @Test
    public void dedentFalsePreservesIndentation() throws IOException {
        String body = render("ImportWidgetTest/extension/README-dedent-false.src.md");
        // Original indentation (4 spaces from class body) is preserved
        assertTrue(body.contains("    public void greet() {"), body);
    }

    @Test
    public void langOverrideUsesGivenLanguage() throws IOException {
        String body = render("ImportWidgetTest/extension/README-lang-override.src.md");
        assertTrue(body.contains("```go"), body);
        assertFalse(body.contains("```java"), body);
    }

    @Test
    public void conflictingLinesAndRegionLogsErrorAndProducesEmpty() throws IOException {
        String body = render("ImportWidgetTest/extension/README-conflict-lines-region.src.md");
        // Widget output is empty; surrounding shell should not contain class content
        assertFalse(body.contains("public void greet()"));
        assertFalse(body.contains("public class WithRegion"));
    }

    @Test
    public void regionNotFoundLogsErrorAndProducesEmpty() throws IOException {
        String body = render("ImportWidgetTest/extension/README-region-not-found.src.md");
        assertFalse(body.contains("public void greet()"));
    }

    @Test
    public void consecutiveImportsPreserveBlankLineSeparator() throws IOException {
        // Regression: the widget's default path (run-generator=true, wrap=false) must
        // preserve the trailing newline of imported content. Otherwise two consecutive
        // ${widget:import} calls collapse into adjacent lines with no blank line
        // between them, which breaks GitHub's markdown rendering when one fragment
        // ends in an HTML block and the next begins with a heading.
        String body = render("ImportWidgetTest/extension/README-two-imports.src.md");
        String sep = System.lineSeparator();
        assertTrue(body.contains("FIRST" + sep + sep + "SECOND"), body);
    }
}